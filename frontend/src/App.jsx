import { useEffect, useState } from "react";
import api from "./services/api";
import "./index.css";

function App() {
  const [dashboard, setDashboard] = useState({
    members: 0,
    books: 0,
    issued: 0,
    overdue: 0,
  });

  useEffect(() => {
    loadDashboard();
  }, []);

  const loadDashboard = async () => {
    try {
      const response = await api.get(
        "/method/library_management.library.api.dashboard.get_dashboard"
      );

      setDashboard(response.data.message);
    } catch (error) {
      console.error("Error loading dashboard:", error);
    }
  };

  return (
    <div className="container">
      <h1>📚 Library Management System</h1>

      <div className="cards">
        <div className="card">
          <h2>Total Members</h2>
          <h1>{dashboard.members}</h1>
        </div>

        <div className="card">
          <h2>Total Books</h2>
          <h1>{dashboard.books}</h1>
        </div>

        <div className="card">
          <h2>Books Issued</h2>
          <h1>{dashboard.issued}</h1>
        </div>

        <div className="card">
          <h2>Overdue Books</h2>
          <h1>{dashboard.overdue}</h1>
        </div>
      </div>

      <h2>Quick Actions</h2>

      <div className="buttons">
        <button>Add Member</button>
        <button>Add Book</button>
        <button>Issue Book</button>
        <button>Return Book</button>
        <button>Overdue Report</button>
      </div>
    </div>
  );
}

export default App;